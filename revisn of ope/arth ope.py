num_1 = int(input("enter the number 1 :"))
num_2 = int(input("enter the number 2 :"))
operator = input("enter the operator  :")

if operator == "+":
    print(num_1+ num_2)
elif operator =="-":
    print(num_1 - num_2)
elif operator == "*":
    print(num_1* num_2)
elif operator == "/":
    print(num_1 / num_2)
elif operator =="**":
    print(num_1 **num_2)
elif operator =="%":
    print(num_1% num_2)





