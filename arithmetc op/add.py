

num_1 = int(input("Enter the number_1"))
num_2 = int(input("Enter the number_2"))
operator = input("Enter the operator:")


if operator == "+":
    print(num_1+ num_2)
elif operator == "-":
    print(num_1-num_2)
elif operator == "*":    
    print(num_1 * num_2)
elif operator == "/":    
    print(num_1 / num_2)  
elif operator == "**":    
    print(num_1 ** num_2)  
elif operator == "%":    
    print(num_1 % num_2)           










